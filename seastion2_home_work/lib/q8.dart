/*
8. Write a Dart program that declares two numeric variables and performs addition, subtraction,
multiplication, division, and modulo on them. Print each result.
*/
void main() {
  int x = 10;
  int y = 7;
  int addition = x + y;
  print('the addition of x + y = $addition');

  int subtraction = x - y;
  print('the subtarction of x - y = $subtraction');

  int multiplication = x * y;
  print('the multiplication of x * y = $multiplication');

  double division = x / y;
  print('the division of x / y = $division');

  int modulo = x % y;
  print('the modulo of x % y = $modulo');
}
